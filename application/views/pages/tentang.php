                    <h1 class="text-center" style="color: black;"><b>KOMINFO</b></h1>
                    <p>Kementerian Komunikasi dan Informatika, sebelumnya bernama "Departemen Penerangan" (1945-1999), "Kementerian Negara Komunikasi dan Informasi" (2001-2005), dan Departemen Komunikasi dan Informatika (Depkominfo) (2005-2009).</p>
                    <p>Setelah proklamasi kemerdekaan dibentuk Lembaga Penerangan yang secara fungsional menjalankan kebijakan, pola dan pedoman penerangan dengan tujuan (1) membela dan mempertahankan kemerdekaan, (2) mengajak rakyat agar turut serta mempertahankan dan mengisi kemerdekaan serta (3) memperkenalkan Republik Indonesia di dan ke luar negeri.  Selama periode 1959-1965, sesuai Haluan Pembangunan Nasional sebagai ketetapan MPRS, Departemen Penerangan dibentuk untuk menyelenggarakan penerangan melalui media penerangan antara lain radio, film, toestel dan foto, percetakan, kendaraan, mesin stensil, dan mesin ketik.</p>
                    <p>Mulai tahun 1966, salah satu tugas pokok organisasi penerangan adalah mengarahkan pendapat umum agar terbentuk dukungan, kontrol dan pratisipasi sosial yang positif terhadap pelaksanaan kebijaksanaan pemerintah, selain untuk penerangan ke dalam dan luar negeri. Pada masa itu pengorganisasian ke dalam dilakukan agar Departemen Penerangan berfungsi menjadi Juru Bicara Pemerintah. Tanggal 15 September 1967 wewenang penerangan luar negeri (Penlugri) yang sejak 1959 dipegang Departemen Luar Negeri RI dialihkan kembali pengelolaannya kepada Departemen Penerangan.</p>
                    <p>Tahun 1971, untuk meningkatkan efisiensi dan efektivitas sarana media massa, dikembangkan sistem komunikasi terintegrasi melalui koordinasi, integrasi dan sinergi antarunsur-unsur penerangan pemerintah. Dibentuklah lembaga antara lain Badan Koordinasi Kehumasan Pemerintah (BAKOHUMAS) dan Badan Koordinasi Penerangan (BAKOPEN).</p>
                    <p>Di tingkat daerah, dibentuk jawatan penerangan provinsi, kantor penerangan kabupaten dan juru penerang di tingkat kecamatan. Di kabupaten dan kota dibentuk Pusat Penerangan Masyarakat (PUSPENMAS) dengan kegiatan utama penerangan antar pribadi didukung sarana penerangan di daerah seperti: radio, televisi, film penerangan, pers penerbitan, pameran dan pertunjukkan rakyat serta diskusi kerja.</p>
